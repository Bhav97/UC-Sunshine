public class MainActivity extends AppCompatActivity {

    private final static String FORECASTFRAGMENT_TAG = "TAG";

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);
        if(savedInstanceState == null) {
            getSupportFragmentManager().beginTransaction()
                    .add(R.id.fragment, new ForecastFragment(),FORECASTFRAGMENT_TAG)
                    .commit();
        }
    }

    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        // Inflate the menu; this adds items to the action bar if it is present.
        getMenuInflater().inflate(R.menu.main, menu);
        return true;
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        // Handle action bar item clicks here. The action bar will
        // automatically handle clicks on the Home/Up button, so long
        // as you specify a parent activity in AndroidManifest.xml.
        int id = item.getItemId();

        //noinspection SimplifiableIfStatement
        if (id == R.id.action_settings) {
            Intent preference = new Intent(MainActivity.this , SettingsActivity.class);
            startActivity(preference);
            return true;
        } else if (id == R.id.action_map) {
            openPreferredLocationInMap();
            return true;
        }

        return super.onOptionsItemSelected(item);
    }

    private void openPreferredLocationInMap() {
        SharedPreferences prefs = getSharedPreferences("sunshineprefs", Context.MODE_PRIVATE);
        String location = Utility.getPreferredLocation(this);
        Uri geoLocation = Uri.parse("geo:0?0").buildUpon()
                .appendQueryParameter("q",location)
                .build();
        Intent map = new Intent(Intent.ACTION_VIEW);
        map.setData(geoLocation);
        if(map.resolveActivity(getPackageManager())!=null) {
            startActivity(map);
        } else Toast.makeText(MainActivity.this, "No map app found", Toast.LENGTH_SHORT).show();
    }
}
